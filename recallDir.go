package domovoi

////////////////////////////////////////////////////////////////////////////////////////////////////

import (
	"os"

	"github.com/DanielRivasMD/horus"
)

////////////////////////////////////////////////////////////////////////////////////////////////////

// RecallDir returns the current working directory.
// On failure, it returns a wrapped error with diagnostic details using Horus.
func RecallDir() (string, error) {
	currentDir, err := os.Getwd()
	if err != nil {
		return "", horus.NewCategorizedHerror(
			"get working directory",
			"cwd_error",
			"failed to get current working directory",
			err,
			map[string]any{"function": "RecallDir"},
		)
	}
	return currentDir, nil
}

