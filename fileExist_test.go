func ExampleFileExist_basic() {
	exists, _ := FileExist("no-such-file", nil, false)
	fmt.Println(exists)
	// Output:
	// false
}

func ExampleFileExist_autoCreate() {
	create := func(path string) (bool, error) {
		f, err := os.Create(path)
		if err != nil {
			return false, err
		}
		defer f.Close()
		f.WriteString("# default\n")
		return true, nil
	}

	exists, _ := FileExist("example.conf", create, false)
	fmt.Println(exists)
	// Output:
	// true
}

